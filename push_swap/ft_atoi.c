#include "push_swap.h"

int	ft_atoi(char *arr)
{
	int	i;
	int	p;
	int	num;

	i = 0;
	p = 1;
	num = 0;
	while (arr[i] <= ' ')
		i++;
	if (arr[0] == '-' || arr[0] == '+')
	{
		if (arr[0] == '-')
			p = -1;
		i++;
	}
	while (arr[i] >= '0' && arr[i] <= '9')
	{
		num = num * 10 + (arr[i] - 48);
		i++;
	}
	return (num * p);
}
